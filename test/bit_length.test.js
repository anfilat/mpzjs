const {MPZ} = require('../');

test('bit length', () => {
  const bl = MPZ(
    '433593290010590489671135819286259593426549306666324008679782084292'
      + '2446494189019075159822930571858728009485237489829138626896756141'
      + '873895833763224917704497568647701157104426'
  ).bitLength();

  expect(bl > 0).toEqual(true);
});
